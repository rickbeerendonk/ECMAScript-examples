// 5 << 1 = 10 (0101 becomes 1010, multiply by 2)
console.log(5 << 1); // 10

// 5 << 2 = 20 (0101 becomes 10100, multiply by 4)
console.log(5 << 2); // 20

// 3 << 3 = 24 (0011 becomes 11000, multiply by 8)
console.log(3 << 3); // 24

// Left shift by 0
console.log(5 << 0); // 5
